# Sumar 2 números naturales a y b (CAMILO)
def suma(a, b):
    return a+b
# Restar 2 números naturales a y b (CARLOS)
def resta(a, b):
    return a-b
# Multiplicar 2 números naturales a y b (YURLEY)
def multiplica(a, b):
    y = a*b
    return y	
# Divide 2 números naturales a y b (JHON)
def dividir(a, b):
    g = a/b
    return g
